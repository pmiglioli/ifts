public class EsempioConcatenazione {
	
	
	/**
	 * @param one
	 * @param two
	 * @return
	 */
	public String concatenate(String one, String two) {
		return one + two;
	}
	
	public String getString(String stringa) {
		return stringa;
	}
	
	
}
